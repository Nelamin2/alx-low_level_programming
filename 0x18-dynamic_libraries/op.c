#include <stdio.h>
/**
 * add- adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int add(int a, int b)
{
int sum;
sum = a + b;
return (sum);
}
/**
 * sub- subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: sub
 */
int sub(int a, int b)
{
int sub;
sub = a - b;
return (sub);
}
/**
 * mul- multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: mul
 */
int mul(int a, int b)
{
int mul;
mul = a * b;
return (mul);
}
/**
 * div- divides two integers
 * @a: first integer
 * @b: second integer
 * Return: div
 */
int div(int a, int b)
{
int div;
div = a / b;
return (div);
}
/**
 * mod- two integers
 * @a: first integer
 * @b: second integer
 * Return: mod
 */
int mod(int a, int b)
{
int mod;
mod = a % b;
return (mod);
}
